fun main(){
    val day = 1
    when (day){
        1 ->{
            println("First day of teh week")
            println("Monday")
        }
        2 ->{
            println("Second day of teh week")
            println("Tuesday")
        }
        3 ->{
            println("Third day of teh week")
            println("Wednesday")
        }
        4 -> println("Thursday")
        5 -> println("Friday")
        6 -> println("Saturday")
        7 -> println("Sunday")
        else -> println("Invalid day")
    }
}
